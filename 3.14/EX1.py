x=10
y="문자열"
print(f"숫자 data:{x}, 문자 data :{y}")
print(f"[{x:<10}]")
print(f"[{x:^10}]")
print(f"[{x:>10}]")
print("채움문자와 숫자")
print(f"[{1:5d}][{-2:05d}][{3:05d}]")
print(f"[{1:#<5d}][{-2:#^5d}][{3:#>5d}]")
print(f"[{1:=5d}][{-2:=5d}][{3:=5d}]")
print(f"[{1:=05d}][{-2:=05d}][{3:=05d}]")
print(f"[{1:#=5d}][{-2:#=5d}][{3:#=5d}]")

print("숫자 표기 형식")
print(f"[{100000000:=5,}][{-200000000:=5,}][{300000000:=5,}]")
print(f"[{100000000:=5_}][{-200000000:=5_}][{300000000:=5_}]")
print(f"[{100000000:=5e}][{-200000000:=5e}][{300000000:=5e}]")

print(f"[{10:5b}][{-20:5b}][{30:5b}]")
print(f"[{10:5o}][{-20:5o}][{30:5o}]")
print(f"[{10:5x}][{-20:5x}][{30:5x}]")
print(f"[{10:5X}][{-20:5x}][{30:5x}]")

print(f"[{{{10}%}}],{{}},[{-20}%],%%[{30}%]")
